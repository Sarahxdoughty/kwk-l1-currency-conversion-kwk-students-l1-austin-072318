def usd_to_eur (dollars)
  return dollars/1.2
end

def eur_to_usd (euros)
  return euros*1.2
end

def usd_to_jpy (dollars)
  return dollars*111.1
end

def jpy_to_usd (yen)
  return yen/111.1 
end

